function runBioPopup(element){
  var element = jQuery(element);

  var name = element.parents('.profile').find('.pb-name').text();
  var title = element.parents('.profile').find('.pb-title').text();
  var content = element.parents('.profile').find('.pb-content').html();

  jQuery('#profile-bio').find('#profile-bio-name').text(name);
  jQuery('#profile-bio').find('#profile-bio-title').text(title);
  jQuery('#profile-bio').find('#profile-bio-content').html(content);

  jQuery('#profile-bio').modal({
    'show': true,
    'backdrop': false,
  })
}